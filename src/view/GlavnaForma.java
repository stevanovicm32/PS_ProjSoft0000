    private void popuniComboBoxRecima() {
        jComboBox1.removeAllItems();
        List<SRB_REC> listaSrb =new ArrayList<>();
        listaSrb=Controller.getInstance().vratiListuReci();
        for (SRB_REC srb_rec : listaSrb) {
            jComboBox1.addItem(srb_rec);
        }
    }

    private void popuniComboBoxJezicima() {
        jComboBox2.removeAllItems();
        List<Jezik> listaJezika =new ArrayList<>();
        listaJezika= Controller.getInstance().vratiListuJezika();
        for (Jezik jezik : listaJezika) {
            jComboBox2.addItem(jezik);
        }
    }
