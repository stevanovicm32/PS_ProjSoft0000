    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int selektovaniRed=jTable1.getSelectedRow();
        if(selektovaniRed==-1)
        {
            JOptionPane.showMessageDialog(this, "Morate odabrati red", "Greska", JOptionPane.ERROR_MESSAGE);
        }
        else
        {
            Prevod p=Controller.getInstance().vratiListuPrevoda().get(selektovaniRed);
            Controller.getInstance().obrisiOdabranuRed(p);
            TableModelRec tmr=new TableModelRec(Controller.getInstance().vratiListuPrevoda());
            jTable1.setModel(tmr);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

